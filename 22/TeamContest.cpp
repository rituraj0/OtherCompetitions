class TeamContest 
{
public:

int  worstRank(vector <int> stren) 
{

  sort( stren.begin() , stren.begin()+3 );
  sort( stren.begin()+3 , stren.end() );
  
  int my=stren[0]+stren[2];
  
  int rank=1;
  
  int i=3 , j=stren.size()-1;
  
  while( i < j )
  {
        if( stren[i]+stren[j] > my )
          {  rank++;
              i+=2;
              j--;
          }
        else
            i+=3;
   }
   
   return rank;
	
}

};

double test0() {
	int t0[] = {5, 7, 3, 5, 7, 3, 5, 7, 3};
	vector <int> p0(t0, t0+sizeof(t0)/sizeof(int));
	TeamContest * obj = new TeamContest();
	clock_t start = clock();
	int my_answer = obj->worstRank(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
